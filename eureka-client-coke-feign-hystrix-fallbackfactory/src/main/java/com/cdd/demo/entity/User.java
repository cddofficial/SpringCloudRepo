package com.cdd.demo.entity;
 
public class User {
 
	private Long id;
	
	private String name;
	
	private Short age;
 
	public Long getId() {
		return id;
	}
 
	public void setId(Long id) {
		this.id = id;
	}
 
	public String getName() {
		return name;
	}
 
	public void setName(String name) {
		this.name = name;
	}
 
	public Short getAge() {
		return age;
	}
 
	public void setAge(Short age) {
		this.age = age;
	}
	
}